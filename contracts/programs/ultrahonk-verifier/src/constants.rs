//! UltraHonk verification constants
//!
//! These constants match the reference implementation (miquelcabot/ultrahonk_verifier)
//! but are tuned for our circuit sizes (log_circuit_size <= 16).

/// Maximum log of circuit size we support
/// Our circuits are small, so we use 16 instead of the full 28
pub const MAX_LOG_CIRCUIT_SIZE: usize = 16;

/// Number of subrelations in UltraHonk
pub const NUMBER_OF_SUBRELATIONS: usize = 26;

/// Batched relation partial length for non-ZK proofs
pub const BATCHED_RELATION_PARTIAL_LENGTH: usize = 8;

/// Number of entities (polynomials evaluated at sumcheck challenge point)
pub const NUMBER_OF_ENTITIES: usize = 40;

/// Number of unshifted polynomials
pub const NUMBER_UNSHIFTED: usize = 35;

/// Number of alpha challenges (relation separators)
pub const NUMBER_OF_ALPHAS: usize = NUMBER_OF_SUBRELATIONS - 1; // 25

/// Number of VK commitment points
pub const VK_NUM_COMMITMENTS: usize = 27;

/// Scalar size (BN254 Fr)
pub const SCALAR_SIZE: usize = 32;

/// G1 point size in proof format (4 × 32 bytes for split coordinates)
pub const G1_PROOF_POINT_SIZE: usize = 128;

/// G1 point size in affine format (2 × 32 bytes)
pub const G1_AFFINE_SIZE: usize = 64;

/// G2 point size in affine format (4 × 32 bytes)
pub const G2_AFFINE_SIZE: usize = 128;

/// VK metadata size (circuit_size + log_circuit_size + num_public_inputs + pub_inputs_offset)
pub const VK_METADATA_SIZE: usize = 4 * 8; // 4 u64s = 32 bytes

/// Total VK size: metadata + 27 G1 points (affine)
pub const VK_SIZE: usize = VK_METADATA_SIZE + VK_NUM_COMMITMENTS * G1_AFFINE_SIZE; // 32 + 1728 = 1760

/// Proof size for our circuits (log_n = 16)
/// Wire commitments: 4 × 128 = 512
/// Lookup helpers: 4 × 128 = 512
/// Sumcheck univariates: 16 × 8 × 32 = 4096
/// Sumcheck evaluations: 40 × 32 = 1280
/// Gemini fold comms: 15 × 128 = 1920
/// Gemini evaluations: 16 × 32 = 512
/// Shplonk Q + KZG quotient: 2 × 128 = 256
/// Total: ~9088 bytes (varies with circuit size)
pub const PROOF_SIZE_LOG16: usize =
    4 * G1_PROOF_POINT_SIZE +      // wire commitments w1-w4
    4 * G1_PROOF_POINT_SIZE +      // lookup helpers (read_counts, read_tags, inverses, z_perm)
    (NUMBER_OF_ENTITIES + BATCHED_RELATION_PARTIAL_LENGTH * MAX_LOG_CIRCUIT_SIZE) * SCALAR_SIZE + // sumcheck
    (MAX_LOG_CIRCUIT_SIZE - 1 + 2) * G1_PROOF_POINT_SIZE + // gemini fold + shplonk Q + KZG
    MAX_LOG_CIRCUIT_SIZE * SCALAR_SIZE; // gemini evaluations

/// Barycentric Lagrange denominators for sumcheck evaluation
/// These are precomputed: d_i = prod_{j != i}(i - j) for i in 0..8
pub const BARYCENTRIC_LAGRANGE_DENOMINATORS: [i64; BATCHED_RELATION_PARTIAL_LENGTH] = [
    1,    // d_0 = 1 * 2 * 3 * 4 * 5 * 6 * 7 = 5040
    -1,   // d_1 = (-1) * 1 * 2 * 3 * 4 * 5 * 6 = -720
    2,    // d_2 = (-2) * (-1) * 1 * 2 * 3 * 4 * 5 = 240
    -6,   // d_3 = ...
    24,
    -120,
    720,
    -5040,
];

