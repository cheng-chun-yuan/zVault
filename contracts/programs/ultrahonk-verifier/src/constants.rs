#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_vk_size() {
        // VK size should be 1760 bytes
        assert_eq!(VK_SIZE, 1760);
    }

    #[test]
    fn test_constants_match_reference() {
        // These should match the reference implementation
        assert_eq!(NUMBER_OF_SUBRELATIONS, 26);
        assert_eq!(BATCHED_RELATION_PARTIAL_LENGTH, 8);
        assert_eq!(NUMBER_OF_ENTITIES, 40);
        assert_eq!(NUMBER_OF_ALPHAS, 25);
    }
}
